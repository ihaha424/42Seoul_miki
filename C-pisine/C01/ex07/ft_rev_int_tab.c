#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;

	temp = 0;
	while (temp < size / 2)
	{
		ft_swap(&tab[temp], &tab[size - 1 - temp]);
		temp++;
	}
}
